namespace Gy2021001Template
{
    public class GameItemTemplate : GameThingTemplateBase
    {
        public GameItemTemplate()
        {

        }

        public GameItemTemplate(Guid id):base(id)
        {

        }

        /// <summary>
        /// 显示名称。
        /// </summary>
        public string DisplayName { get; set; }

        /// <summary>
        /// 游戏内Id服务器不使用。
        /// </summary>
        public int? GId { get; set; }

        /// <summary>
        /// 所属的Id字符串，以逗号分隔。
        /// </summary>
        public string GenusIdString { get; set; }


        private List<Guid> _GenusIds;

        /// <summary>
        /// 获取所属的属Id集合。
        /// </summary>
        [NotMapped]
        public List<Guid> GenusIds
        {
            get
            {
                lock (this)
                    if (null == _GenusIds)
                    {
                        _GenusIds = GenusIdString.Split(',', StringSplitOptions.RemoveEmptyEntries).Select(c => Guid.Parse(c)).ToList();
                        ;
                    }
                return _GenusIds;
            }
        }

    }
